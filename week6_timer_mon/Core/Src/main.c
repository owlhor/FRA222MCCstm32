/* USER CODE BEGIN 4 */
void encoderSpeedReaderCycle() {
	//get DMA Position form number of data
	uint32_t CapPos =CAPTURENUM -  __HAL_DMA_GET_COUNTER(htim2.hdma[TIM_DMA_ID_CC1]);
	uint32_t sum = 0 ;

	//calculate diff from all buffer except current dma
	for(register int i=2 ;i < CAPTURENUM-1;i++)
	{
		DiffTime[i]  = capturedata[(CapPos+1+i)%CAPTURENUM]-capturedata[(CapPos+i)%CAPTURENUM];
		//Sum all  Diff
		sum += DiffTime[i];
	}

	//mean all Diff
	MeanTime =sum / (float)(CAPTURENUM-3);
}

void SpeedMainShaft(){
	// 1 mini round = 12 P/R
	// 64 mini round = 1 main round = 728 P/R = 1 RPM

	//MeanTime = 1 cycle time
	// PulseNum per min = 60M / MeanTime
	// Pulsenum / 64*12 = 1 MainRound
	//MeanTime * PulseNum = 1 minute = 6M usec
	RoundNum = (60000000 / MeanTime)/728;

	if(micros() - timestampx >= 10000) // 1msec
			{
				timestampx = micros();
				RoundNum2nd = pulseCnt * (728/60);
				pulseCnt = 0;
			}

}

