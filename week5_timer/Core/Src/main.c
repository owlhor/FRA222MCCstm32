/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART2_UART_Init();
  MX_ADC1_Init();
  MX_TIM1_Init();
  MX_TIM3_Init();
  MX_TIM11_Init();
  MX_TIM10_Init();
  /* USER CODE BEGIN 2 */
  ///////////////////////////////////////////////////////////
  // start ADC, TIMER
  HAL_ADC_Start_IT(&hadc1); // Start_IT = start interrupt
  HAL_TIM_Base_Start(&htim3);

  // timer, OC gen PWM
  HAL_TIM_Base_Start(&htim1);
  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);

  //timer for micros()
  HAL_TIM_Base_Start_IT(&htim11);
  HAL_TIM_Base_Start_IT(&htim10);
  //ADCConfigINIT();// multichannel ADC

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */

	  //ADCReadUpdate();

	  	  millisec = millis();
	  //2KHz loop use timer
	  if(micros() - TimeOutputLoop > 500){
		  TimeOutputLoop = micros(); // stamp
		  __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, PWMOut);
		  //ADC_Target = ADCFeedx[1].datt;
	  }
	  // control PWM to get 1V=> 1023 ADu
	  if(ADCUpdateFlag){
		  // any task when update flag = 1
		  // PID
		  Err[0] = ADC_Target - ADCFeedback;

		  PWMOut = PWMOut + (k_p * Err [0]) + (k_i * Integral) + (k_d *(Err[1]-Err[0]) );

		  // saturation
		  if(PWMOut >= 10000){
			  PWMOut = 10000;
		  }
		  else if(PWMOut <= 0){
			  PWMOut = 0;
		  }
		  else{
			  Integral += Err[0];
		  }
		  Err[1] = Err[0];

		  ADCUpdateFlag = 0; //end task
	  	 }

  }
  /* USER CODE END 3 */
}

